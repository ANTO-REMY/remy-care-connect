const alphaSort = <T extends { name: string }>(items: T[]) =>
  [...items].sort((a, b) => a.name.localeCompare(b.name));

export const NAIROBI_SUB_COUNTIES = alphaSort(RAW_SUB_COUNTIES);

export const NAIROBI_WARDS = alphaSort(RAW_WARDS);

export const NAIROBI_WARDS_BY_SUB_COUNTY = RAW_SUB_COUNTIES.reduce<Record<number, Ward[]>>(
  (acc, subCounty) => {
    acc[subCounty.id] = alphaSort(
      RAW_WARDS.filter((ward) => ward.sub_county_id === subCounty.id)
    );
    return acc;
  },
  {}
);
